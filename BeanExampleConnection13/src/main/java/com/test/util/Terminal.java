package com.test.util;

public class Terminal {

	String name;
	int termnumber;

	public Terminal(String name, int termnumber) {
		super();
		this.name = name;
		this.termnumber = termnumber;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getTermNumber() {
		return termnumber;
	}

	public void setNumber(int termnumber) {
		this.termnumber = termnumber;
	}

}
